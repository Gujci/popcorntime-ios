extension UIImage {
    
    class func addToFavoritesImage() -> UIImage? {
        return UIImage(named: "AddToFavoritesIcon")
    }
    
    class func removeFromFavoritesImage() -> UIImage? {
        return UIImage(named: "RemoveFromFavoritesIcon")
    }
    
}
